package pakash;

public class EjercicioPropuesto04_OperadoresDeAsignacion {

	public static void main(String[] args) {
		int valor = 3;
		System.out.println("El valor inicial de la variable es: " + valor);
		System.out.println("**********************************");
		valor += 5;
		System.out.println("Si le sumamos 5 el nuevo valor es: " + valor);
		System.out.println("**********************************");
		valor *= 4;
		System.out.println("Multiplicado por 4 el nuevo valor ahora es: " + valor);
		System.out.println("**********************************");
		valor /=2;
		System.out.println("Al dividirlo por 2 ahora la variable vale: " + valor);
	}

}
